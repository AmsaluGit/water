                 ->select('p , c')
                 ->join('p.category','c')
                 ->where("c.id = p.category")
                 ;
                 

        if($search)
            $qb->andWhere("p.name  LIKE '%".$search."%'")
              // ->andwhere("c.name  LIKE '%".$search2."%'")
               ;

               return 
            $qb->orderBy('p.id', 'ASC')
               ->getQuery()
            
